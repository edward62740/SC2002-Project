package enums;

/**
 * The {@link UserRole} enumeration represents different roles that a user can have.
 * <ul>
 * <li>{@link #STUDENT}: The user has the role of a student.</li>
 * <li>{@link #STAFF}: The user has the role of a staff member.</li>
 * <li>{@link #CCM}: The user has the role of a committee member (CCM).</li>
 * </ul>
 */
public enum UserRole {
    /**
     * The user has the role of a student.
     */
    STUDENT,

    /**
     * The user has the role of a staff member.
     */
    STAFF,

    /**
     * The user has the role of a committee member (CCM).
     */
    CCM
}
